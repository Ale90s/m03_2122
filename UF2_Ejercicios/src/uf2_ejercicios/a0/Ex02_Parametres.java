package uf2_ejercicios.a0;

public class Ex02_Parametres {

    public static void main(String[] args) {
        System.out.println(sumaNumeros(3, 5));
        System.out.println(sumaNumeros(6, 10));
        System.out.println(sumaNumeros(34985, 4555));
    }

    static int sumaNumeros(int n1, int n2) {
        return (n1 + n2);
    }
}
