package javacourse.Section4_ExpressionsStatementsCode_BlocksMethods;

public class KeyWordAndExpressions {
    
    public static void main(String[] args) {
        
        int test = 5;
        
        if (test == 5) {
            
            System.out.println("What's an Expression?");
        }
        
        /*
            In this case expressions are "test = 5" in the first line, the data type (int)
            and the semicolon (;) doesn't count as expressions.
            After that, in the if statement, the expression is "test == 5"
            and in the last command, the expression is "What's an Expression=?"
        */



    }
}
